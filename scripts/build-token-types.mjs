#!/usr/bin/env node
/**
 * build-token-types.mjs
 *
 * Generates TypeScript-facing token contracts from the token JSON sources.
 * These files are committed on purpose and checked in CI so agents can import
 * constrained token surfaces without reading the whole token graph.
 */
import { existsSync, mkdirSync, mkdtempSync, readFileSync, rmSync, readdirSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { tmpdir } from 'node:os';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outDir = join(root, 'packages/tokens/dist');
const componentsOutDir = join(outDir, 'components');
const check = process.argv.includes('--check');
const header = '// GENERATED by scripts/build-token-types.mjs — do not edit\n\n';

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

function leaves(node, prefix = []) {
  const out = [];
  for (const [key, value] of Object.entries(node ?? {})) {
    if (value && typeof value === 'object' && '$value' in value) {
      out.push([prefix.concat(key).join('.'), value]);
    } else if (value && typeof value === 'object') {
      out.push(...leaves(value, prefix.concat(key)));
    }
  }
  return out;
}

function quotedUnion(values) {
  return values.length ? values.map((value) => `  | ${JSON.stringify(value)}`).join('\n') : '  | never';
}

function pascalCase(value) {
  return value
    .split(/[^a-z0-9]+/i)
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join('');
}

function camelTokenName(path) {
  const parts = path.split('.').slice(1);
  const base = parts
    .flatMap((part) => part.split(/[^a-z0-9]+/i).filter(Boolean))
    .map((part, index) => {
      const lower = part.toLowerCase();
      return index === 0 ? lower : lower[0].toUpperCase() + lower.slice(1);
    })
    .join('');
  return /^[a-zA-Z_$]/.test(base) ? base : `token${base}`;
}

function uniqueNames(paths) {
  const seen = new Map();
  return paths.map((path) => {
    const base = camelTokenName(path);
    const count = seen.get(base) ?? 0;
    seen.set(base, count + 1);
    return count === 0 ? base : `${base}${count + 1}`;
  });
}

function write(path, content) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content);
}

function generatedFiles(dir) {
  const files = [];
  if (!existsSync(dir)) return files;
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) files.push(...generatedFiles(full));
    else files.push(full);
  }
  return files.sort();
}

function renderAll(targetDir) {
  const primitives = readJson(join(root, 'packages/tokens/src/primitives.json'));
  const semanticLight = readJson(join(root, 'packages/tokens/src/semantic-light.json'));
  const semanticDark = readJson(join(root, 'packages/tokens/src/semantic-dark.json'));
  const components = readJson(join(root, 'packages/tokens/src/components.generated.json'));

  const primitivePaths = leaves(primitives).map(([path]) => path).sort();
  const semanticPaths = [...new Set([...leaves(semanticLight), ...leaves(semanticDark)].map(([path]) => path))].sort();
  const componentEntries = Object.entries(components).sort(([a], [b]) => a.localeCompare(b));
  const componentPaths = componentEntries.flatMap(([component, value]) => leaves(value).map(([path]) => `${component}.${path}`)).sort();
  const componentNames = componentEntries.map(([name]) => name);

  write(join(targetDir, 'tokens.d.ts'), `${header}` +
    `export type PrimitiveTokenPath =\n${quotedUnion(primitivePaths)};\n\n` +
    `export type SemanticTokenPath =\n${quotedUnion(semanticPaths)};\n\n` +
    `export type ComponentTokenPath =\n${quotedUnion(componentPaths)};\n\n` +
    `export type TokenPath = PrimitiveTokenPath | SemanticTokenPath | ComponentTokenPath;\n\n` +
    `export type ComponentTokenNamespace =\n${quotedUnion(componentNames)};\n`);

  for (const [component, value] of componentEntries) {
    const paths = leaves(value).map(([path]) => `${component}.${path}`).sort();
    const names = uniqueNames(paths);
    const typeName = `${pascalCase(component)}Token`;
    const constName = `${component.replace(/[^a-z0-9]+([a-z0-9])/gi, (_, char) => char.toUpperCase())}Tokens`;
    const lines = [];
    lines.push(header.trimEnd());
    lines.push('');
    lines.push(`export type ${typeName} =`);
    lines.push(`${quotedUnion(paths)};`);
    lines.push('');
    lines.push(`export const ${constName} = {`);
    for (let i = 0; i < paths.length; i++) {
      lines.push(`  ${names[i]}: ${JSON.stringify(paths[i])},`);
    }
    lines.push(`} as const satisfies Record<string, ${typeName}>;`);
    lines.push('');
    lines.push(`export const ${constName}List = Object.values(${constName});`);
    lines.push('');
    write(join(targetDir, 'components', `${component}-tokens.ts`), `${lines.join('\n')}`);
  }
}

function compareDirectories(expectedDir, actualDir) {
  const expected = generatedFiles(expectedDir).map((file) => relative(expectedDir, file)).sort();
  const actual = generatedFiles(actualDir)
    .filter((file) => file.endsWith('tokens.d.ts') || file.endsWith('-tokens.ts'))
    .map((file) => relative(actualDir, file))
    .sort();
  const all = [...new Set([...expected, ...actual])].sort();
  const diffs = [];
  for (const file of all) {
    const expectedPath = join(expectedDir, file);
    const actualPath = join(actualDir, file);
    if (!existsSync(expectedPath) || !existsSync(actualPath)) {
      diffs.push(file);
      continue;
    }
    if (readFileSync(expectedPath, 'utf8') !== readFileSync(actualPath, 'utf8')) diffs.push(file);
  }
  return diffs;
}

if (check) {
  const temp = mkdtempSync(join(tmpdir(), 'clementine-token-types-'));
  try {
    renderAll(temp);
    const diffs = compareDirectories(temp, outDir);
    if (diffs.length) {
      console.error(`token types: ${diffs.length} generated file(s) out of date`);
      for (const file of diffs.slice(0, 20)) console.error(`  - packages/tokens/dist/${file}`);
      if (diffs.length > 20) console.error(`  … ${diffs.length - 20} more`);
      process.exit(1);
    }
    console.log('✔︎ token types: generated TypeScript token exports are current');
  } finally {
    rmSync(temp, { recursive: true, force: true });
  }
} else {
  rmSync(join(outDir, 'tokens.d.ts'), { force: true });
  rmSync(componentsOutDir, { recursive: true, force: true });
  renderAll(outDir);
  const count = generatedFiles(componentsOutDir).filter((file) => file.endsWith('-tokens.ts')).length;
  console.log(`✔︎ token types: wrote tokens.d.ts + ${count} component token module(s)`);
}
